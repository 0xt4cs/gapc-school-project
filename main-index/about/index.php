          <div class="col-lg-6 about-text">
            <h3 class="gapc-custom-border">Motto</h3>
            <p>
              Don Andres Pascual had a strong love for his fellow countrymen,
              which led him to establish the Wesleyan High School Foundation
              in 1947. He put up the school on his own property in Tangos,
              Navotas, Metro Manila. His goal was to build a modern high
              school building on a one-hectare site that he had reserved, but
              he passed away before realizing this dream. His wife, Dona Adela
              S. Pascual, and their niece, Dr. Olivia S. Pascual, were
              determined to make it happen, and through hard work and prayer,
              they founded the Governor Andres Pascual High School on June 14,
              1976, in memory of Don Andres Pascual. The school grew from 384
              students in 1976 to 2,395 students in 1995, and in July 1993, it
              expanded to offer college courses. Today, the school is known as
              the Governor Andres Pascual College, which now has 10 courses,
              including Master of Arts in Education and Doctor of Education.
              <br />
              The Governor Andres Pascual College is a testament to Don Andres
              Pascual's vision and the dedication of his family to continue
              his legacy. They hope to one day see it become a university,
              "The Governor Andres Pascual Universities." The college's
              success is attributed to the hardworking staff, students, and
              the blessings that God has bestowed upon them. They attribute
              everything to Him and seek to glorify His name.
            </p>
            <h3 class="gapc-custom-border">Mission</h3>
            <p>
              Imploring the aid of the divine providence, the Governor Andres
              Pascual College provides a unique combination of academic
              excellence and Christian education. The College thrives on the
              principles that knowledge is acquired through discipline,
              competence is established when knowledge is developed when this
              competence is exercised for the benefits of others.
            </p>
            <h3 class="gapc-custom-border">Vision</h3>
            <p>
              To build to better future for the youth by providing education
              with low tuition fees. This effort will serve as inspiration of
              compassionate and committed institution when character is
              developed with intellect that foster success and life long
              learning.
            </p>
